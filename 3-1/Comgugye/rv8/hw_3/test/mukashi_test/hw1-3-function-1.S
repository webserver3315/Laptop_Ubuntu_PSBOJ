    slli t1, a1, 3
    add t1, t1, a0
    ld t3, 0(t1)

    slli t2, a2, 3
    add t2, t2, a0
    ld t4, 0(t2)

    sd t3, 0(t2)
    sd t4, 0(t1)
    jalr zero, 0(ra)

